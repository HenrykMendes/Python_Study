#4.Faça um Programa que peça as 4 notas bimestrais e mostre a média.

valor_01 = int (input (f"Digite 01: "))
valor_02 = int (input (f"Digite 02: "))
valor_03 = int (input (f"Digite 03: "))
valor_04 = int (input (f"Digite 04: "))

